package org.webcastellum;

public class CaptchaGenerationException extends java.lang.Exception {
    
    public CaptchaGenerationException() {
    }
    public CaptchaGenerationException(String msg) {
        super(msg);
    }
    public CaptchaGenerationException(Throwable cause) {
        super(cause);
    }
    public CaptchaGenerationException(String msg, Throwable cause) {
        super(msg, cause);
    }
    
}
